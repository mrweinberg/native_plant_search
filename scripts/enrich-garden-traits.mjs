let missing = 0
const missingIds = []
const updated = plants.map((p) => {
  const t = G[p.id]
  if (!t) {
    missing++
    missingIds.push(p.id)
    return { ...p, soilPh: p.soilPh ?? ['neutral'], spreadHabit: p.spreadHabit ?? 'clump', springEphemeral: p.springEphemeral ?? false }
  }
  return { ...p, ...t }
})
writeFileSync(PLANTS_PATH, JSON.stringify(updated, null, 2) + '\n')
console.log(`Updated ${plants.length} plants. ${missing} had no entry (defaulted).`)
